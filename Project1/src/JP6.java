public class JP6 {

    public static void main(String[] args) {

        float x = 50.0F;
        float y = 20.0F;

        System.out.println("Add: " + (x + y));
        System.out.println("Sub: " + (x - y));
        System.out.println("Mul: " + (x * y));
        System.out.println("Div: " + (x / y));
        System.out.println("Mod: " + (x % y));
    }
}

